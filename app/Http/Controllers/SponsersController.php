     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $cur = Sponsor::find($id);
        switch($cur->main){
            case 'No':
                Sponsor::where('id', $id)->update(['main' => 'Yes']);
                return back();
            case 'Yes':
                Sponsor::where('id', $id)->update(['main' => 'No']);
                return back();
        }

        return back();
        
    }

